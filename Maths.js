// Math methods sample code

// Math.abs() - returns the absolute value of a number
const absValue = Math.abs(-5);
console.log(absValue); // Output: 5

// Math.ceil() - rounds a number up to the nearest integer
const ceilValue = Math.ceil(4.3);
console.log(ceilValue); // Output: 5

// Math.floor() - rounds a number down to the nearest integer
const floorValue = Math.floor(4.7);
console.log(floorValue); // Output: 4

// Math.round() - rounds a number to the nearest integer
const roundValue = Math.round(4.5);
console.log(roundValue); // Output: 5

// Math.random() - generates a random number between 0 and 1
const randomValue = Math.random();
console.log(randomValue); // Output: a random number between 0 and 1

// Math.max() - returns the largest of zero or more numbers
const maxValue = Math.max(10, 5, 8, 12);
console.log(maxValue); // Output: 12

// Math.min() - returns the smallest of zero or more numbers
const minValue = Math.min(10, 5, 8, 12);
console.log(minValue); // Output: 5